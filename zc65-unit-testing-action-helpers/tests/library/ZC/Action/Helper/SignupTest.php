class ZC_Action_Helper_SignupTest
    extends ControllerTestCase
{
    public function test_Can_Get_Signup_Instance()
    {
        $helper = new ZC_Action_Helper_Signup();
        $this->assertType('ZC_Action_Helper_Signup',$helper);
    }

    public function test_Can_Get_Signup_In_Html_View()
    {
        $this->dispatch('/');
        $this->assertAction('index');
        $this->assertContains('sign up', $this->getResponse()->getBody());
    }

    public function test_Can_Get_Logged_In_Html_View_From_Get()
    {
        $this->getRequest()->setParam('loggedIn', '1');
        $this->dispatch('/');
        $this->assertAction('index');
        $this->assertContains('You are logged in', $this->getResponse()->getBody());
    }

    public function test_Can_Get_Null_View_From_Non_Dispatched_Controller()
    {
        $helper = new ZC_Action_Helper_Signup();
        $controller = new IndexController($this->getRequest(),
                                          $this->getResponse(), array());
        $helper->setActionController($controller);
        $this->assertEquals(null,$helper->getView());
   
    }
    
    public function test_Can_Get_View_From_Action_Helper()
    {
        $helper = new ZC_Action_Helper_Signup();
        $this->dispatch('/');

        $controller = new IndexController($this->getRequest(),
                                          $this->getResponse(), array());

        $helper->setActionController($controller);
        
        $this->assertType('Zend_View',$helper->getView());


    }
    public function test_Can_Get_LoggedIn_String_As_View_Variable()
    {
        $helper = new ZC_Action_Helper_Signup();
        $this->getRequest()->setParam('loggedIn','1');
        $this->dispatch('/');
        $controller = new IndexController($this->getRequest(),
                                          $this->getResponse(), array());
        $helper->setActionController($controller);
        $this->assertEquals('You are logged in', $helper->getView()->signup);

    }
}